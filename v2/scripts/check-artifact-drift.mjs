/**
 * READ-ONLY artifact drift scan (Loop B) for the Goods Alignment Engine.
 * Companion to check-canon-drift.mjs (Loop A). Run from v2/:
 *
 *   node scripts/check-artifact-drift.mjs            (warn-only on staleness)
 *   node scripts/check-artifact-drift.mjs --strict   (also fail on staleness)
 *
 * Layer 2 of the engine. It reads the artifact register
 * (src/lib/data/artifact-register.json) and the canon registry
 * (src/lib/data/canon.ts) and answers the question Layer 1 makes possible:
 * when a canon fact moves, which artifacts need re-checking?
 *
 *   1. CITATION INTEGRITY: every artifact's citesCanon id resolves to a real
 *      canon fact. A typo or a removed fact is a hard fail (the register would
 *      otherwise silently stop tracking something).
 *   2. STALENESS BY CITATION: if any cited canon fact has an asAt newer than the
 *      artifact's lastVerified, the artifact is flagged for review (its copy may
 *      still carry the old number). This is the Loop B signal.
 *   3. REVERSE INDEX: prints, per canon fact, which artifacts cite it, so a
 *      human changing a number knows the blast radius up front.
 *
 * It changes nothing. The review queue is written to wiki/canon/artifact-review.md.
 * Hard citation breaks exit 1; staleness warns (exits 1 under --strict).
 */
import { readFile, mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, '..'); // v2/
const dataDir = path.join(projectRoot, 'src', 'lib', 'data');
const canonDir = path.resolve(projectRoot, '..', 'wiki', 'canon');
const STRICT = process.argv.includes('--strict');

// ── Load canon facts (id -> asAt) by parsing canon.ts as text. The interface
// uses `id: string` (no quote) so only the data entries `id: 'x'` match. ──
const canonText = await readFile(path.join(dataDir, 'canon.ts'), 'utf8');
const ids = [...canonText.matchAll(/\bid:\s*'([^']+)'/g)].map((m) => m[1]);
const asAts = [...canonText.matchAll(/\basAt:\s*'([^']+)'/g)].map((m) => m[1]);
if (ids.length === 0 || ids.length !== asAts.length) {
  console.error(`Could not parse canon.ts cleanly (${ids.length} ids, ${asAts.length} asAts). Aborting.`);
  process.exit(2);
}
const canonAsAt = new Map(ids.map((id, i) => [id, asAts[i]]));

const { artifacts } = JSON.parse(await readFile(path.join(dataDir, 'artifact-register.json'), 'utf8'));

const olderThan = (a, b) => new Date(a) < new Date(b); // a is older than b

const broken = []; // hard: citesCanon id not in canon
const stale = []; // warn: cited fact moved after lastVerified
const reverse = new Map(ids.map((id) => [id, []])); // canon id -> [artifact titles]

for (const art of artifacts) {
  for (const fid of art.citesCanon || []) {
    if (!canonAsAt.has(fid)) {
      broken.push({ artifact: art.title, id: art.id, fid });
      continue;
    }
    reverse.get(fid).push(art.title);
    if (olderThan(art.lastVerified, canonAsAt.get(fid))) {
      stale.push({ artifact: art.title, id: art.id, fid, factAsAt: canonAsAt.get(fid), lastVerified: art.lastVerified });
    }
  }
}

// ── Console ──
console.log(`Goods artifact drift scan (Loop B)\n`);
console.log(`${artifacts.length} artifacts, ${ids.length} canon facts.\n`);
console.log('Reverse index (change a fact -> these artifacts need review):');
for (const id of ids) {
  const who = reverse.get(id);
  if (who.length) console.log(`  ${id.padEnd(22)} ${who.length}: ${who.join('; ')}`);
}
console.log('');

if (stale.length) {
  console.log('Artifacts to review (a cited fact moved after lastVerified):');
  for (const s of stale) {
    console.log(`  ${s.artifact}  [cites ${s.fid} @ ${s.factAsAt} > verified ${s.lastVerified}]`);
  }
  console.log('');
}

// ── Review queue file ──
const byArtifact = new Map();
for (const s of stale) {
  if (!byArtifact.has(s.artifact)) byArtifact.set(s.artifact, []);
  byArtifact.get(s.artifact).push(`${s.fid} (canon ${s.factAsAt} > verified ${s.lastVerified})`);
}
const queue = [
  `# Artifact review queue (Loop B)`,
  '',
  'Generated by scripts/check-artifact-drift.mjs. Each artifact below cites a canon fact that changed after the artifact was last verified. Re-check the artifact against canon.ts, fix any stale number, then bump its lastVerified in src/lib/data/artifact-register.json.',
  '',
  broken.length ? '## Broken citations (fix first)' : '## Broken citations\nNone. Every citesCanon id resolves to a canon fact.',
  ...broken.map((b) => `- ${b.artifact} (${b.id}) cites unknown canon id "${b.fid}"`),
  '',
  byArtifact.size ? '## Stale artifacts (review)' : '## Stale artifacts\nNone. Every artifact was verified at or after each cited fact moved.',
  ...[...byArtifact.entries()].map(([art, reasons]) => `- **${art}**\n${reasons.map((r) => `  - ${r}`).join('\n')}`),
  '',
].join('\n');
await mkdir(canonDir, { recursive: true });
await writeFile(path.join(canonDir, 'artifact-review.md'), queue);

if (broken.length) {
  console.error('BROKEN CITATIONS — artifact-register.json cites canon ids that do not exist:');
  for (const b of broken) console.error(`  - ${b.artifact} (${b.id}) -> "${b.fid}"`);
  console.error(`\nFix the citesCanon ids in src/lib/data/artifact-register.json to match canon.ts.`);
  process.exit(1);
}
if (stale.length) {
  console.warn(`${byArtifact.size} artifact(s) need review (see wiki/canon/artifact-review.md).`);
  if (STRICT) process.exit(1);
}
console.log(`OK — all citations resolve.${stale.length ? ` ${byArtifact.size} artifact(s) queued for review.` : ' No stale artifacts.'}`);
