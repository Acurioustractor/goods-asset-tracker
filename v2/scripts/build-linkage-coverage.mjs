// Generates wiki/investor/13-linkage-coverage.md: the master join of
// voices x quotes x photos x videos x communities x registry, with gaps.
// Read-only; re-run any time: node --env-file=.env.local scripts/build-linkage-coverage.mjs

import fs from 'node:fs';
import path from 'node:path';

const ROOT = path.resolve(import.meta.dirname, '..');
const OUT = path.join(ROOT, '..', 'wiki', 'investor', '13-linkage-coverage.md');
const URL = (process.env.NEXT_PUBLIC_SUPABASE_URL || '').replace(/"/g, '');
const KEY = (process.env.SUPABASE_SERVICE_ROLE_KEY || '').replace(/"/g, '');
const H = { apikey: KEY, Authorization: `Bearer ${KEY}` };
const get = async (q) => (await fetch(`${URL}/rest/v1/${q}`, { headers: H })).json();

const voiceData = JSON.parse(fs.readFileSync(path.join(ROOT, 'src/lib/data/voice-impact-data.json'), 'utf8'));
const registrySrc = fs.readFileSync(path.join(ROOT, 'src/lib/data/storyteller-registry.ts'), 'utf8');

const [links, contacts, communities] = await Promise.all([
  get('media_links?select=media_type,target_type,target_key,media_key'),
  get('crm_contacts?select=id,name'),
  get('communities?select=id,name'),
]);
const nameById = new Map(contacts.map((c) => [c.id, c.name]));
const commById = new Map(communities.map((c) => [c.id, c.name]));

const media = new Map(); // person name -> {photos, videos}
for (const l of links) {
  if (l.target_type !== 'person') continue;
  const name = nameById.get(l.target_key);
  if (!name) continue;
  const m = media.get(name) ?? { photos: 0, videos: 0 };
  if (l.media_type === 'video') m.videos += 1;
  else m.photos += 1;
  media.set(name, m);
}
const commMedia = new Map();
for (const l of links) {
  if (l.target_type !== 'community') continue;
  commMedia.set(l.target_key, (commMedia.get(l.target_key) ?? 0) + 1);
}

const inRegistry = (name) =>
  registrySrc.includes(`name: '${name}'`) || registrySrc.includes(`name: "${name}"`);

const COMMUNITY_MATCH = [
  [/tennant/i, 'tennant-creek'], [/utopia|arlparra|wenitong/i, 'utopia'], [/alice/i, 'alice-springs'],
  [/palm island/i, 'palm-island'], [/maningrida/i, 'maningrida'], [/kalgoorlie|goldfields/i, 'kalgoorlie'],
  [/darwin/i, 'darwin'], [/katherine/i, 'katherine'], [/kununurra/i, 'kununurra'], [/mount isa|mt isa/i, 'mt-isa'],
];
// Voices whose EL location is empty or unmatchable; placement from registry/analysis.
const NAME_COMMUNITY = {
  'Margaret Lloyd': 'utopia', // Ben-confirmed Utopia Margaret; homeland "Wenitong" (as transcribed)
  'Mark': 'palm-island',
  'Ana - Bega': 'kalgoorlie', // practitioner voice, Goldfields context
};
const commFor = (loc, name) => {
  if (NAME_COMMUNITY[name]) return NAME_COMMUNITY[name];
  const hay = `${loc ?? ''} ${name}`;
  for (const [re, id] of COMMUNITY_MATCH) if (re.test(hay)) return id;
  return null;
};

const rows = voiceData.voices.map((v) => {
  const quotes = v.transcripts.flatMap((t) => t.topQuotes);
  const m = media.get(v.name) ?? { photos: 0, videos: 0 };
  return {
    name: v.name,
    flags: [v.held && 'HELD', v.staff && 'STAFF', v.funder && 'FUNDER', v.isElder && 'ELDER'].filter(Boolean).join(' '),
    community: commFor(v.location, v.name),
    transcripts: v.transcriptCount,
    words: v.totalWords,
    quotes: quotes.length,
    cleared: quotes.filter((q) => q.cleared).length,
    deck: quotes.filter((q) => q.strength === 'deck').length,
    photos: m.photos,
    videos: m.videos,
    portrait: !!v.portrait,
    registry: inRegistry(v.name),
  };
});

const gaps = [];
for (const r of rows) {
  if (r.flags.includes('HELD') || r.flags.includes('STAFF')) continue;
  const g = [];
  if (!r.portrait) g.push('no EL portrait');
  if (r.photos === 0) g.push('no linked photos');
  if (r.videos === 0) g.push('no linked video');
  if (r.cleared === 0) g.push('no cleared quotes');
  if (!r.registry) g.push('NOT in storyteller registry');
  if (!r.community) g.push('community unmapped');
  if (g.length) gaps.push(`- **${r.name}**: ${g.join(', ')}`);
}

const commVoices = new Map();
for (const r of rows) if (r.community) commVoices.set(r.community, (commVoices.get(r.community) ?? 0) + 1);

const totalLinks = links.length;
const md = `# Area 13 — Linkage coverage (voices x quotes x media x communities)

> AUTO-GENERATED by \`v2/scripts/build-linkage-coverage.mjs\` — do not hand-edit; re-run after
> any clearing pass, media link, or new transcript. Generated: 2026-07-20.
> Sources: voice-impact-data.json, media_links (${totalLinks} rows), crm_contacts, communities,
> storyteller-registry.ts.

## Per-voice coverage

| Voice | Flags | Community | T | Words | Quotes | Cleared | Deck | Photos | Videos | Portrait | Registry |
|---|---|---|---|---|---|---|---|---|---|---|---|
${rows.map((r) => `| ${r.name} | ${r.flags || ''} | ${r.community ? commById.get(r.community) : '—'} | ${r.transcripts} | ${r.words.toLocaleString()} | ${r.quotes} | ${r.cleared} | ${r.deck} | ${r.photos} | ${r.videos} | ${r.portrait ? 'yes' : 'NO'} | ${r.registry ? 'yes' : 'NO'} |`).join('\n')}

## Community coverage

| Community | Voices in model | Media links |
|---|---|---|
${communities.filter((c) => commVoices.has(c.id) || commMedia.has(c.id)).map((c) => `| ${c.name} | ${commVoices.get(c.id) ?? 0} | ${commMedia.get(c.id) ?? 0} |`).join('\n')}

## Gaps (community voices only; held/staff excluded)

${gaps.join('\n')}

## Totals

- Voices: ${rows.length} (${rows.filter((r) => !r.flags.includes('HELD') && !r.flags.includes('STAFF') && !r.flags.includes('FUNDER')).length} community, 1 funder witness, 1 staff, 1 held)
- Quotes: ${rows.reduce((n, r) => n + r.quotes, 0)} extracted, ${rows.reduce((n, r) => n + r.cleared, 0)} cleared, ${rows.reduce((n, r) => n + r.deck, 0)} deck-grade
- media_links: ${totalLinks} (${links.filter((l) => l.target_type === 'person').length} person / ${links.filter((l) => l.target_type === 'community').length} community / ${links.filter((l) => l.target_type === 'story').length} story)
`;
fs.writeFileSync(OUT, md);
console.log(`${rows.length} voices, ${gaps.length} gap lines -> ${path.relative(path.join(ROOT, '..'), OUT)}`);
